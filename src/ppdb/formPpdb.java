    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed
       String value = inputCaptcha.getText();
        if(!value.matches("[0-9]+") ) {
            JOptionPane.showMessageDialog(null, "Isi Captcha dengan Angka!", "validasi", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if(captchaClass.validasi( Integer.valueOf(value)) == false ){
            JOptionPane.showMessageDialog(null, "Captcha Tidak Sesuai!", "validasi", JOptionPane.INFORMATION_MESSAGE);
            
            buatCaptcha();
            
            inputCaptcha.getText();
            return;
        }
        
        try {
                st = cn.createStatement();
                st.executeUpdate("UPDATE ppdb set " 
                    + "nis='"       + inputNis.getText() + "', "
                    + "nama='"      + inputNama.getText() + "', "
                    + "jenis_kelamin='"   + inputJK.getSelectedItem() + "', "
                    + "tempat_lahir='"      + inputTempat.getText() + "', "
                    + "tanggal_lahir='"      + inputTanggal.getText() + "', "
                    + "alamat='"      + inputAlamat.getText() + "', "
                    + "asal_sekolah='"      + inputAsal.getText() + "', "
                    + "kelas='"        + inputKelas.getSelectedItem() + "', "
                    + "jurusan='"        + inputJurusan.getSelectedItem() + "'"
                            + " WHERE nis='" + inputNis.getText() + "'");
                tampilData("");
                JOptionPane.showMessageDialog(null, "Update Berhasil");
                buatCaptcha();
                reset();
            } catch (Exception e) {
                e.printStackTrace();
            }
    }//GEN-LAST:event_buttonUpdateActionPerformed

    
