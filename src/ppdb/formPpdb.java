    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        String value = inputCaptcha.getText();
        if(!value.matches("[0-9]+") ) {
            JOptionPane.showMessageDialog(null, "Hanya bisa di Input dengan Angka!", "WARNING!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if(captchaClass.validasi( Integer.valueOf(value)) == false ){
            JOptionPane.showMessageDialog(null, "Captcha Tidak Sesuai!", "WARNING!", JOptionPane.INFORMATION_MESSAGE);
            
            buatCaptcha();
            
            inputCaptcha.getText();
            return;
        }
        
        try {
            st = cn.createStatement();
            st.executeUpdate("INSERT INTO ppdb VALUES(null, '" + inputNis.getText() + "','"
                + inputNama.getText() + "','"
                + inputJK.getSelectedItem() + "','"
                + inputTempat.getText() + "','"
                + inputTanggal.getText() + "','"
                + inputAlamat.getText() + "','"
                + inputAsal.getText() + "','"
                + inputKelas.getSelectedItem() + "','"
                + inputJurusan.getSelectedItem() 
                + "')");
            
            tampilData("");
            JOptionPane.showMessageDialog(null, "Berhasil Disimpan!");
            inputNis.setText("");
            inputNama.setText("");
            inputJK.setSelectedItem("");
            inputTempat.setText("");
            inputTanggal.setText("");
            inputAlamat.setText("");
            inputAsal.setText("");
            inputKelas.setSelectedItem("");
            inputJurusan.setSelectedItem("");
            inputCaptcha.setText("");
            buatCaptcha();
          } catch (Exception e) {
            e.printStackTrace();
          }
    }//GEN-LAST:event_buttonSimpanActionPerformed

